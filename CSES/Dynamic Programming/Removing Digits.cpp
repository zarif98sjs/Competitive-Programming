const int nmax = 1e6+7;
const int INF = 1e9;

template <class T>
string to_str(T x)
{
    stringstream ss;
    ss<<x;
    return ss.str();
}


int dp[nmax];

int solve(int n)
{
    if(n==0) return 0;

    int &ret = dp[n];
    if(~ret) return ret;

    ret = INF;

    string temp = to_str(n);

    for(auto ch:temp)
    {
        ret = min(ret,1+solve(n-(ch-'0')));
    }

    return ret;
}

void solveTC()
{
    int n;
    cin>>n;

    memset(dp,-1,sizeof dp);
    cout<<solve(n)<<endl;
}

int32_t main()
{
    optimizeIO();

    int tc = 1;
//    cin>>tc;

    while(tc--)
    {
        solveTC();
    }

    return 0;
}

