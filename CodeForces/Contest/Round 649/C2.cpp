int main()
{
    optimizeIO();

    int n;
    cin>>n;

    for(int i=1;i<=n;i++)
        cin>>ara[i] , freq[ara[i]]++;

    bool ok = true;

    for(int i=1;i<=n;i++)
        if(ara[i]>i)
        {
            ok = false;
            break;
        }

    if(!ok)
    {
        cout<<-1<<endl;
        return 0;
    }

    vector<int>possible;

    for(int i=0;i<=100000;i++)
        if(freq[i]==0)
            possible.push_back(i);

    vector<int>ans(n+10);

    int id = 0;

    ans[1] = possible[id];
    id++;

    for(int i=2;i<=n;i++)
    {
        if(ara[i]!=ara[i-1])
            ans[i] = ara[i-1];
        else
            ans[i] = possible[id] , id++;

    }

    for(int i=1;i<=n;i++)
        cout<<ans[i]<<" ";

    return 0;
}

