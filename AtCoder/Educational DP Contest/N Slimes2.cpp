LL dp[nmax][nmax];

LL sum(int l,int r,const vector<LL> &v)
{
    LL s=0;
    for(int i=l;i<=r;i++)
        s += v[i];
    return s;
}

int main()
{
    optimizeIO();

    int tc = 1;

    while(tc--)
    {
        int n;
        cin>>n;

        vector<LL>v(n);

        for(int i=0;i<n;i++)
            cin>>v[i];

        for(int l=n-1;l>=0;l--)
        {
            for(int r=0;r<n;r++)
            {
                if(l==r) dp[l][r] = 0;
                else
                {
                    dp[l][r] = LLONG_MAX;
                    LL cost = sum(l,r,v);

                    for(int i=l;i<r;i++)
                        dp[l][r] = min(dp[l][r],dp[l][i] + dp[i+1][r] + cost);
                }
            }
        }

        cout<<dp[0][n-1]<<endl;
    }

    return 0;
}

