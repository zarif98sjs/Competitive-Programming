{
    optimizeIO();

    int n;
    cin>>n;

    vector<LL>v(n);
    for(int i=0;i<n;i++)
        cin>>v[i];

    memset(dp,-1,sizeof dp);
    cout<<solve(0,n-1,v)<<endl;

    return 0;
}

