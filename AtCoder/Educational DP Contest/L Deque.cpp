LL dp[nmax][nmax];

LL solve(int l,int r,const vector<LL>&v)
{
    LL &ret = dp[l][r];

    if(ret!=-1) return ret;

    if(l==r) return ret = v[l];

    ret = max(v[l]-solve(l+1,r,v),v[r]-solve(l,r-1,v));
    return ret;
}

