bool solveTC()
{
    int n;
    cin>>n;

    vector<PII>v(n);
    for(int i=0;i<n;i++)
        cin>>v[i].F>>v[i].S;

    for(int i=0;i<n-2;i++)
    {
        if(v[i].F==v[i].S && v[i+1].F==v[i+1].S && v[i+2].F==v[i+2].S )
            return true;
    }

    return false;
}

